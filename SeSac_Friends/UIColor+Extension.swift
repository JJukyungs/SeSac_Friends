import Foundation
import UIKit

extension UIColor {
    
    // Black & White
    class var blackColor: UIColor? { return UIColor(named: "black")}
    class var whiteColor: UIColor? { return UIColor(named: "white")}
    
    // Brand Color
    class var greenColor: UIColor? { return UIColor(named: "green")}
    class var whitegreenColor: UIColor? { return UIColor(named: "whitegreen")}
    class var yellowgreenColor: UIColor? { return UIColor(named: "yellowgreen")}
    class var gray1Color: UIColor? { return UIColor(named: "gray1")}
  
    // Grayscale
    class var gray2Color: UIColor? { return UIColor(named: "gray2")}
    class var gray3Color: UIColor? { return UIColor(named: "gray3")}
    class var gray4Color: UIColor? { return UIColor(named: "gray4")}
    class var gray5Color: UIColor? { return UIColor(named: "gray5")}
    class var gray6Color: UIColor? { return UIColor(named: "gray6")}
    class var gray7Color: UIColor? { return UIColor(named: "gray7")}
   
    
    // System Color
    class var errorColor: UIColor? { return UIColor(named: "error")}
    class var focusColor: UIColor? { return UIColor(named: "focus")}
    class var successColor: UIColor? { return UIColor(named: "success")}
                                                    
                                                
    
    
}
